const MAX_POINTS: u32 = 100_000;
fn main() {
	println!("The value of MAX_POINTS is: {}", MAX_POINTS);
	let mut x = 5;
	println!("The value of x is: {}", x);
	x = 6;
	println!("The value of x is: {}", x);
	let spaces = "    ";
	println!("The value of spaces is: {}", spaces);
	let spaces = spaces.len();
	println!("The value of spaces is: {}", spaces);
}
